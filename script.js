let currentLang = "it";

function applyLang(lang){
  currentLang = lang;
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang][key] !== undefined){
      el.innerHTML = translations[lang][key];
    }
  });
  document.querySelectorAll(".lang-toggle [data-lang-it]").forEach(el => {});
  const toggleLabel = document.getElementById("lang-toggle-label");
  if (toggleLabel){
    toggleLabel.innerHTML = lang === "it"
      ? 'IT <span style="opacity:.4">/</span> <b>EN</b>'
      : '<b>IT</b> <span style="opacity:.4">/</span> EN';
  }
  localStorage.setItem("gl-lang", lang);
}

document.addEventListener("DOMContentLoaded", () => {
  const saved = localStorage.getItem("gl-lang");
  const browserLang = navigator.language?.startsWith("it") ? "it" : "en";
  applyLang(saved || browserLang);

  const langBtn = document.getElementById("lang-toggle");
  if (langBtn){
    langBtn.addEventListener("click", () => {
      applyLang(currentLang === "it" ? "en" : "it");
    });
  }

  const navToggle = document.getElementById("nav-toggle-btn");
  const navLinks = document.getElementById("nav-links");
  if (navToggle && navLinks){
    navToggle.addEventListener("click", () => {
      navLinks.classList.toggle("open");
    });
    navLinks.querySelectorAll("a").forEach(a => {
      a.addEventListener("click", () => navLinks.classList.remove("open"));
    });
  }
});

/* ==========================================================================
   Signature element: animated Hamiltonian-path grid
   Used both large (hero background) and small (project visual)
   ========================================================================== */
function initGridAnimation(canvasId, opts = {}){
  const canvas = document.getElementById(canvasId);
  if (!canvas) return;
  const ctx = canvas.getContext("2d");
  const size = opts.gridSize || 10;
  const speed = opts.speed || 0.0022;
  const dotColor = "rgba(255,255,255,0.06)";
  const lineColor = opts.lineColor || "111,214,224";

  let dpr = Math.min(window.devicePixelRatio || 1, 2);
  let w, h, cell;

  function resize(){
    const rect = canvas.parentElement.getBoundingClientRect();
    w = rect.width; h = rect.height;
    canvas.width = w * dpr;
    canvas.height = h * dpr;
    canvas.style.width = w + "px";
    canvas.style.height = h + "px";
    ctx.setTransform(dpr, 0, 0, dpr, 0, 0);
    cell = Math.min(w, h) / (size + 1.6);
  }
  resize();
  window.addEventListener("resize", resize);

  // Generate a snake (boustrophedon) Hamiltonian path over the grid
  const path = [];
  for (let row = 0; row < size; row++){
    if (row % 2 === 0){
      for (let col = 0; col < size; col++) path.push([col, row]);
    } else {
      for (let col = size - 1; col >= 0; col--) path.push([col, row]);
    }
  }

  const offsetX = () => (w - (size - 1) * cell) / 2;
  const offsetY = () => (h - (size - 1) * cell) / 2;

  let t = (Math.random() * path.length) | 0;

  function draw(){
    ctx.clearRect(0, 0, w, h);
    const ox = offsetX(), oy = offsetY();

    // dots
    ctx.fillStyle = dotColor;
    for (let row = 0; row < size; row++){
      for (let col = 0; col < size; col++){
        ctx.beginPath();
        ctx.arc(ox + col * cell, oy + row * cell, Math.max(1.4, cell * 0.05), 0, Math.PI * 2);
        ctx.fill();
      }
    }

    // traveling glow segment along the path
    const trail = 26;
    for (let i = 0; i < trail; i++){
      const idx = Math.floor(t) - i;
      if (idx < 1) continue;
      const a = path[((idx - 1) % path.length + path.length) % path.length];
      const b = path[(idx % path.length + path.length) % path.length];
      const alpha = (1 - i / trail) * 0.9;
      ctx.strokeStyle = `rgba(${lineColor},${alpha})`;
      ctx.lineWidth = Math.max(1.6, cell * 0.09);
      ctx.lineCap = "round";
      ctx.beginPath();
      ctx.moveTo(ox + a[0] * cell, oy + a[1] * cell);
      ctx.lineTo(ox + b[0] * cell, oy + b[1] * cell);
      ctx.stroke();
    }

    // head dot
    const headIdx = ((Math.floor(t) % path.length) + path.length) % path.length;
    const head = path[headIdx];
    ctx.beginPath();
    ctx.fillStyle = `rgba(${lineColor},1)`;
    ctx.shadowColor = `rgba(${lineColor},0.9)`;
    ctx.shadowBlur = cell * 0.5;
    ctx.arc(ox + head[0] * cell, oy + head[1] * cell, cell * 0.09, 0, Math.PI * 2);
    ctx.fill();
    ctx.shadowBlur = 0;

    t += speed * path.length;
    if (t > path.length * 4) t -= path.length * 2;

    requestAnimationFrame(draw);
  }

  const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  if (reduceMotion){
    t = path.length * 0.5;
    draw();
  } else {
    requestAnimationFrame(draw);
  }
}

document.addEventListener("DOMContentLoaded", () => {
  initGridAnimation("project-canvas", { gridSize: 10, speed: 0.03 });
});

/* ==========================================================================
   Active-section nav highlighting + scroll reveal
   ========================================================================== */
document.addEventListener("DOMContentLoaded", () => {
  // Active nav link based on current section in view
  const navMap = {};
  document.querySelectorAll('.nav-links a[href^="#"]').forEach(a => {
    navMap[a.getAttribute("href").slice(1)] = a;
  });
  const sections = document.querySelectorAll("section[id]");
  if (sections.length && "IntersectionObserver" in window){
    const spy = new IntersectionObserver((entries) => {
      entries.forEach(e => {
        if (e.isIntersecting){
          Object.values(navMap).forEach(a => a.classList.remove("active"));
          if (navMap[e.target.id]) navMap[e.target.id].classList.add("active");
        }
      });
    }, { rootMargin: "-45% 0px -50% 0px", threshold: 0 });
    sections.forEach(s => spy.observe(s));
  }

  // Scroll reveal (skipped when the user prefers reduced motion)
  const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  if (!reduceMotion && "IntersectionObserver" in window){
    const revealEls = document.querySelectorAll(
      ".section-head, .studio-grid > *, .project-card, .lab-card, .contact-inner > *, .privacy-body"
    );
    revealEls.forEach(el => el.classList.add("reveal"));
    const ro = new IntersectionObserver((entries, obs) => {
      entries.forEach(e => {
        if (e.isIntersecting){
          e.target.classList.add("is-visible");
          obs.unobserve(e.target);
        }
      });
    }, { threshold: 0.12 });
    revealEls.forEach(el => ro.observe(el));
  }
});
